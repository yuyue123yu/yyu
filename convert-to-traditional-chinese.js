// 简体转繁体函数
function convertToTraditional(text) {
  return text.split('').map(char => s2tMap[char] || char).join('');
}

// 读取文件
const fs = require('fs');
const path = require('path');

const filePath = path.join(__dirname, 'src/contexts/LanguageContext.tsx');
const content = fs.readFileSync(filePath, 'utf-8');

// 提取简体中文翻译
const zhMatch = content.match(/zh: \{[\s\S]*?\n  \},\n  en:/);
if (!zhMatch) {
  console.error('无法找到简体中文翻译字典');
  process.exit(1);
}

const zhContent = zhMatch[0].replace(/,\n  en:$/, '');
const zhLines = zhContent.split('\n');

// 转换为繁体中文
const tcLines = zhLines.map(line => {
  // 跳过第一行 "zh: {"
  if (line.trim() === 'zh: {') {
    return '  tc: {';
  }
  
  // 跳过注释行（不转换）
  if (line.trim().startsWith('//')) {
    return line;
  }
  
  // 转换翻译值
  const match = line.match(/^(\s*'[^']+': ')([^']+)('.*)/);
  if (match) {
    const [, prefix, value, suffix] = match;
    const traditionalValue = convertToTraditional(value);
    return `${prefix}${traditionalValue}${suffix}`;
  }
  
  return line;
});

const tcContent = tcLines.join('\n');

console.log('繁体中文翻译字典已生成！');
console.log('\n请将以下内容添加到 translations 对象中（在 ms 字典后面）：\n');
console.log(tcContent);
